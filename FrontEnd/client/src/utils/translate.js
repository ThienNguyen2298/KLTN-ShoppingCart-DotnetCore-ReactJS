export const TranslateColor = (enColor) => {
    switch(enColor){
        case 1: return "Đen";
        case 2: return "Đỏ";
        case 3: return "Cam";
        case 4: return "Vàng";
        case 5: return "Xanh lá";
        case 6: return "Xanh dương";
        case 7: return "Xám";
        case 8: return "Tím";
        case 9: return "Hồng";
        default: return "Trắng";
    }
}